    int calcPontos(equipes eq, char w, int pontos){
        if (w == 'l'){
            if ((eq.getPontos() - pontos) <= 0){
                cont -= 1;
                if (cont == 1){
                    JOptionPane.showMessageDialog(this, "Temos um vencedor!");
                }
            }
            return eq.getPontos() - pontos;
        }else{
            return eq.getPontos() + pontos + 1;
        }
    }
