    void disputa(){
        char h;
        int pontos = Integer.parseInt(aposta.getText());
        equipes p1 = checkParticipants(daVez.getText());
        equipes p2 = checkParticipants(desafiado.getText());
        JOptionPane.showMessageDialog(this, "Preparar!");
        try {
            sleep(2000);
        } catch (Exception e) {
        }
        resp = JOptionPane.showInputDialog(this, "GO");
        h = resp.charAt(0);
        if (h == 'q' || h == 'w'|| h == 'a'|| h == 's'|| h == 'z'|| h == 'x') {
            JOptionPane.showMessageDialog(this, daVez.getText()+" ganhou");
            resp = JOptionPane.showInputDialog("Acertou? (s/n)");
            if (resp.equals("n")){
                resp = JOptionPane.showInputDialog(desafiado.getText()+" acertou? (s/n)");
                if (resp.equals("n")){
                    p1.setPontos(calcPontos(p1, 'l', pontos));
                    p2.setPontos(calcPontos(p2, 'l', pontos));
                }else{
                    p1.setPontos(calcPontos(p1, 'l', pontos));
                    p2.setPontos(calcPontos(p2, 'w', pontos));
                }
            }else{
                p1.setPontos(calcPontos(p1, 'w', pontos));
                p2.setPontos(calcPontos(p2, 'l', pontos));
            }
        } else 
        if (h == '2' || h == '3'|| h == '5'|| h == '6'|| h == '8'|| h == '9'){
            JOptionPane.showMessageDialog(this, desafiado.getText()+" ganhou");
            resp = JOptionPane.showInputDialog("Acertou? (s/n)");
            if (resp.equals("n")){
                resp = JOptionPane.showInputDialog(daVez.getText()+" acertou? (s/n)");
                if (resp.equals("n")){
                    p1.setPontos(calcPontos(p1, 'l', pontos));
                    p2.setPontos(calcPontos(p2, 'l', pontos));
                }else{
                    p1.setPontos(calcPontos(p1, 'w', pontos));
                    p2.setPontos(calcPontos(p2, 'l', pontos));
                }
            }else{
                p1.setPontos(calcPontos(p1, 'l', pontos));
                p2.setPontos(calcPontos(p2, 'w', pontos));
            }    
        }else{
            JOptionPane.showMessageDialog(this, "INVÁLIDO");
            disputa();
        }
    }
